#import "NaviCollectionViewController.h"

@interface NaviCollectionViewController () <UICollectionViewDelegateFlowLayout>
@property (nonatomic, strong) NSMutableArray *arrAddress;
@end

@implementation NaviCollectionViewController

static NSString * const reuseIdentifier = @"Cell";

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Register cell classes
    [self.collectionView registerClass:[UICollectionViewCell class] forCellWithReuseIdentifier:reuseIdentifier];
    
    // Do any additional setup after loading the view.
}

- (void)bindingData:(NSMutableArray *)arr {
    if (arr && arr.count > 0) {
        self.arrAddress = [[NSMutableArray alloc] initWithArray:arr];
        [self.collectionView reloadData];
    }
}
